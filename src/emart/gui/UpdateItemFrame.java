    private void showAllProducts() {
        try {
            List<ProductsPojo> productsList = ProductsDAO.getProductDetails();
            if (productsList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No data available to show", "No data", JOptionPane.ERROR_MESSAGE);
                return;
            }
            btnUpdate.setEnabled(true);
            Object[] rows = new Object[7];
            tb = (DefaultTableModel) jtProductDetails.getModel();
            for (ProductsPojo pros : productsList) {
                rows[0] = pros.getProductId();
                rows[1] = pros.getProductName();
                rows[2] = pros.getProductCompany();
                rows[3] = pros.getProductPrice();
                rows[4] = pros.getOurPrice();
                String tax = pros.getTax() + "%";
                rows[6] = tax;
                rows[5] = pros.getQuantity();
                tb.addRow(rows);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DataBase Error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    
