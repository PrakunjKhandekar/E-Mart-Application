    public boolean validateInput() {
        return !(txtName.getText().trim().isEmpty() || txtProductPrice.getText().trim().isEmpty() || txtOurPrice.getText().trim().isEmpty() || txtCompany.getText().trim().isEmpty() || txtQuantity.getText().trim().isEmpty());
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null,"Please fill all details");
            return;
        }
        
        try{
            int n = jcTax.getSelectedItem().toString().length();
            int tax = Integer.parseInt(jcTax.getSelectedItem().toString().substring(0,n-1));
            ProductsPojo p =new ProductsPojo();
            p.setProductId(txtId.getText().trim());
            p.setProductName(txtName.getText().trim());
            p.setProductCompany(txtCompany.getText().trim());
            p.setProductPrice(Double.parseDouble(txtProductPrice.getText().trim()));
            p.setOurPrice(Double.parseDouble(txtOurPrice.getText().trim()));
            p.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
            p.setTax(tax);
            boolean result = ProductsDAO.updateProduct(p);
            clearFields();
            
            if(result){
                JOptionPane.showMessageDialog(null,"Product update successfull");
                return;
            }
                
            JOptionPane.showMessageDialog(null,"Product updation failed!!");
        }
         catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Database Error", "Error!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(null, "Please insert correct numeric values", "Error!!", JOptionPane.ERROR_MESSAGE);
            n.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

