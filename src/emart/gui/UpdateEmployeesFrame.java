    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        if(!validateInput()){
            JOptionPane.showMessageDialog(null,"Please input every field","Error",JOptionPane.ERROR_MESSAGE);
        }
        try{
            EmployeePojo emp = new EmployeePojo();
            emp.setEmpid(empComBox.getSelectedItem().toString());
            emp.setEmpname(txtEmpName.getText());
            emp.setJob(txtJob.getSelectedItem().toString());
            emp.setSalary(Double.parseDouble(txtSalary.getText()));
            boolean result = EmployeesDAO.updateEmployee(emp);
            if(!result){
                JOptionPane.showMessageDialog(null,"Record not Updated!!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            else
                JOptionPane.showMessageDialog(null,"Record Updated Successfullly!!","Success",JOptionPane.INFORMATION_MESSAGE);
        }
        catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch (NumberFormatException numex) {
                JOptionPane.showMessageDialog(null,"please fill numeric data corectly","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

