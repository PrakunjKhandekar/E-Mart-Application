    private void empComBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empComBoxActionPerformed
        try{
            String empid = empComBox.getSelectedItem().toString();
            EmployeePojo e = EmployeesDAO.getEmpById(empid);
            txtEmpName.setText(e.getEmpname());
            txtSalary.setText(String.valueOf(e.getSalary()));
            txtJob.setSelectedItem(e.getJob());
        }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
        }
    }//GEN-LAST:event_empComBoxActionPerformed

