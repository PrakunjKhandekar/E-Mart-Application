    private void loadReceptionistId() {
        try{
            List<String> receptionistList = ReceptionistDAO.getAllReceptionistUserId();
            if(receptionistList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No receptionist found");
                return;
            }
            btnRemove.setEnabled(true);
            for(String userid:receptionistList){
                jcRecepId.addItem(userid);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error");
            ex.printStackTrace();
        }
    }
