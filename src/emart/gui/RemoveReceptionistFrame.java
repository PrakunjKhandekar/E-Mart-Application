    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int option = JOptionPane.showConfirmDialog(null,"do you wish to continue","Remove Receptionist",JOptionPane.YES_NO_OPTION,JOptionPane.ERROR_MESSAGE);
        String userid = jcRecepId.getSelectedItem().toString();
        if(option==JOptionPane.YES_OPTION){
        try{
            boolean result=ReceptionistDAO.deleteReceptionist(userid);
            if(result){
                JOptionPane.showMessageDialog(null,"Receptionist Deleted succefully");
                jcRecepId.removeItemAt(jcRecepId.getSelectedIndex());
                if(jcRecepId.getItemCount()==0){
                    btnRemove.setEnabled(false);
                }
                return;
            }
            JOptionPane.showMessageDialog(null,"Receptionist cannot be Deleted!!");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error");
            ex.printStackTrace();
        }
        }
        else return;
    }//GEN-LAST:event_btnRemoveActionPerformed

